				<p>Destinations where accommodation, hygiene and sanitation, medical care and water quality are of a high standard pose relatively few serious risks to the health of travelers, except those with pre-existing illness. The same is true of business travelers and tourists visiting most major cities and tourist centers and staying in good-quality accommodation. In contrast, destinations where accommodation is of poor quality, hygiene and sanitation are inadequate, medical services do not exist and clean water is unavailable may pose serious risks for the health of travelers. This applies, for example, to personnel from emergency relief and development agencies and to tourists who venture into remote areas. In these settings, stringent precautions must be taken to avoid illness. Travel warnings from governmental sources should be taken seriously; they are likely to have implications for travel and for travel insurance. </p>

				<p>The epidemiology of infectious diseases in the destination country is of importance to travelers. Travelers and travel medicine practitioners should be aware of the occurrence of these diseases in the destination countries. Unforeseen natural or man-made disasters may occur; outbreaks of known or newly emerging infectious diseases are often unpredictable. New risks to international travelers may arise that are not detailed in this website but will be posted on the WHO web site which should be regularly consulted. Similarly, up-to-date information on safety and security risks should be acquired from authoritative web sites.</p>

				<p>The mode of transport, duration of visit and the behavior and lifestyle of the traveler are important in determining the likelihood of exposure to infectious diseases and will influence decisions on the need for certain vaccinations or anti malaria medication. The duration of visit may also determine whether the traveler is subjected to marked changes in altitude, temperature and humidity or to prolonged exposure to atmospheric pollution.</p>

				<p>Understanding the purpose of the visit and the type of travel planned is critical in relation to the associated travel health risks. However, behavior also plays an important role; for example, going outdoors in the evenings in a malaria-endemic area without taking precautions to avoid mosquito bites may result in the traveler becoming infected with malaria. Exposure to insects, rodents or other animals, infectious agents and contaminated food and water, combined with the absence of appropriate medical facilities, makes travel in many remote regions particularly hazardous. Whatever their destination or mode of travel, it is important that travelers should be aware of the risk of accidents under the influence of alcohol or drugs and, mainly, in relation to road transport or the practice of sports.</p>

